import { Play, PlayElement, PlayFrame, PlayAction, PlayTemplate, ActionType, newId } from "./types"

type CAct = { t: ActionType; e: string; v?: [number, number]; g?: string }
type CFrame = { p: Record<string, [number, number]>; a?: CAct[]; note?: string }
type PD = {
  id: string; n: string; d: string; c: PlayTemplate["category"]
  k?: "attacker" | "defender"; f: CFrame[]
}

function fromDef(def: PD): PlayTemplate {
  return {
    id: def.id, name: def.n, description: def.d, category: def.c,
    create: () => {
      const now = new Date().toISOString()
      const ids = { p1: newId(), p2: newId(), p3: newId(), p4: newId(), p5: newId(), ball: newId() }
      const km: Record<string, string> = { "1": ids.p1, "2": ids.p2, "3": ids.p3, "4": ids.p4, "5": ids.p5, b: ids.ball }
      const kind = def.k ?? "attacker"
      const elements: PlayElement[] = [
        { id: ids.p1, kind, label: "1" }, { id: ids.p2, kind, label: "2" },
        { id: ids.p3, kind, label: "3" }, { id: ids.p4, kind, label: "4" },
        { id: ids.p5, kind, label: "5" }, { id: ids.ball, kind: "ball", label: "" },
      ]
      const frames: PlayFrame[] = def.f.map((f) => ({
        id: newId(),
        positions: Object.fromEntries(Object.entries(f.p).map(([k, [x, y]]) => [km[k], { x, y }])),
        actions: (f.a ?? []).map((a) => {
          const ac: PlayAction = { id: newId(), type: a.t, elementId: km[a.e] }
          if (a.g) ac.targetElementId = km[a.g]
          if (a.v) ac.via = { x: a.v[0], y: a.v[1] }
          return ac
        }),
        note: f.note,
      }))
      return { id: newId(), name: def.n, courtType: "half" as const, elements, frames, createdAt: now, updatedAt: now }
    },
  }
}

