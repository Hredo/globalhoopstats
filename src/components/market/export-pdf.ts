import jsPDF from "jspdf"
import autoTable from "jspdf-autotable"
import { formatEur } from "@/lib/market/league-strength"
import type { Valuation } from "@/lib/market/valuation"

type ReportPlayer = {
  name: string
  position: string | null
  team: string | null
  league: string | null
  valuation: Valuation | null
}

type SimularData = {
  mode: "Simular"
  outgoing: ReportPlayer
  scenarios: {
    verdict: string
    balance: number
    combinedValueEur: number
    incoming: ReportPlayer[]
  }[]
  /** scenario index → AI analysis text */
  analyses?: Record<number, string>
}

type ProponerData = {
  mode: "Proponer"
  outgoing: ReportPlayer[]
  incoming: ReportPlayer[]
  cash: number
  terms: string
  balance: number
  analysis?: string | null
}

